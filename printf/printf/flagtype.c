#include "ft_printf.h"

t_t		*flagtype(t_t *t)
{
	size_t		j;

	j = 0;
	while (t->flagtype[j] != '\0')
	{
		if (t->flagtype[j] == t->trat[t->i])
			t->flag = t->flagtype[j];
		j++;
	}
	return (t);
}
